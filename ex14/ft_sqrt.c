int	ft_sqrt(int nb)
{
	int i;

	i = nb - 1;
	if (nb == 1)
		return (1);
	else if (nb <= 0)
		return (0);
	while (nb % i != 0 && i > 1)
		i--;
	if (i == 1)
		return (0);
	return (i);
}
